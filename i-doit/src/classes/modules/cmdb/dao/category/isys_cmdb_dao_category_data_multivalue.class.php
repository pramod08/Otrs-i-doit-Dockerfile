class isys_cmdb_dao_category_data_multivalue extends isys_cmdb_dao_category_data_value
{

    /**
     * Category value
     *
     * @var array
     */
    public $m_value = [];

    /**
     * String conversion
     *
     * @return string
     */
    public function __toString()
    {
        return implode(', ', $this->m_value);
    }

