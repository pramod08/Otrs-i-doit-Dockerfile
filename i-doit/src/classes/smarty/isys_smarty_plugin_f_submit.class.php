class isys_smarty_plugin_f_submit extends isys_smarty_plugin_f implements isys_smarty_plugin
{
    /**
     * Navigation view.
     *
     * @param   isys_component_template &$p_tplclass
     * @param   array                   $p_param
     *
     * @return  string
     */
    public function navigation_view(isys_component_template &$p_tplclass, $p_param = null)
    {
        if ($p_param === null)
        {
            $p_param = $this->m_parameter;
        } // if

        return (new isys_smarty_plugin_f_button)->set_edit_mode(false)
            ->navigation_view($p_tplclass, $p_param);
    } // function

    /**
     * Edit view.
     *
     * @param   isys_component_template &$p_tplclass
     * @param   array                   $p_param
     *
     * @return  string
     */
    public function navigation_edit(isys_component_template &$p_tplclass, $p_param = null)
    {
        if ($p_param === null)
        {
            $p_param = $this->m_parameter;
        } // if

        return (new isys_smarty_plugin_f_button)->set_edit_mode(true)
            ->navigation_edit($p_tplclass, $p_param);
    } // function
} // class